export function buildInitialWorldPrompt(seed: string, entryTitle: string) {
  return [
    {
      role: "system",
      content:
        "你是一个中文伪纪录百科档案编纂员。你生成的是可持续扩展的本地档案库，不是短篇小说。必须尊重用户给出的题材：可以是写实、历史、科幻、悬疑、超现实、奇幻或混合类型，不要默认转向玄幻。"
    },
    {
      role: "user",
      content: `根据用户的世界种子，创建一个世界观 Wiki 的入口词条。

世界种子：
${seed}

用户指定的入口词条标题：
${entryTitle}

严格输出 JSON，不要输出 Markdown 代码块。JSON 字段：
{
  "worldTitle": "世界名，中文",
  "canonSummary": "不超过 260 字的世界总设定摘要",
  "articleSummary": "不超过 160 字的词条摘要",
  "markdown": "中文 Markdown 词条正文"
}

正文要求：
- 必须像一份冷静的百科档案、调查记录、馆藏说明或地方志条目，不像小说。
- 词条必须围绕用户指定的入口词条，不要擅自更换标题或题材。
- 绝对不要写"在某某世界中""在本设定中""在这条世界线中""该宇宙里"等元叙述；正文必须假定自己描述的是唯一真实历史。
- 不要称呼用户、作者、世界种子、设定、canon 或世界观，不要写“这个故事”“本设定”等出戏措辞。
- 不要套用固定栏目，不要固定使用“摘要/背景/关键关系/争议与未解问题”。
- 按词条对象自然分节：人物可用“生平”“失踪”“档案记载”“评价”；地点可用“地理”“沿革”“行政”“异常记录”；组织可用“沿革”“结构”“活动”“争议”；事件可用“经过”“调查”“影响”“后续处置”；技术或制度可用“原理”“实施”“监管”“事故记录”。
- 第一段直接给出定义、年代、地点、归属或档案编号等硬信息，像已经存在的百科条目。
- 不要输出“相关条目”“参见”“另见”章节；页面会根据正文里的 [[链接]] 自动生成参见列表。
- 首次写到会影响后续设定扩展、值得单独建档的专有名词时，使用 [[中文专名]] 链接。
- 优先链接实体、组织、公司、部门、地点、事件、制度、技术、产品线、项目、作品、人物或案件；不链接普通名词、一次性描写、宽泛类别或当前词条本身。
- 不要为了凑数量堆链接；短词条可以只有少量链接，但包含多个重要专名时不能完全没有内部链接。
- 使用伪纪录片/档案片质感：克制、具体、带出处感，可出现年份、机构名、文件名、修订记录、调查编号、地方俗称，但不要真的列参考文献。
- 如果设定偏写实，保持克制和文献感；如果设定偏科幻，优先使用技术、机构、协议、事故、监管；只有用户种子明显要求时才使用玄幻/魔法意象。
- 不要使用表格。`
    }
  ];
}
